import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression

import warnings
warnings.filterwarnings('ignore')

df= pd.read_csv('https://raw.githubusercontent.com/Kritika201542/project-data/main/Placement_Data_Full_Class.csv')
df.head()

df.info()

df.boxplot(column=['ssc_p','hsc_p','degree_p','etest_p','mba_p'])

# removing outliers from hsc_p
# df=df[df['hsc_p']<80]

ds=df.drop(['sl_no','gender','ssc_b','hsc_b','hsc_s','degree_t','specialisation','salary'], axis=1)
ds.head()

ds.workex.replace(['Yes','No'],[1,0],inplace=True)
ds.status.replace(['Placed','Not Placed'],[1,0],inplace=True)
ds.head()

ds.isnull().sum()

# heatmap
sns.heatmap(ds.corr(),annot=True)

x=ds.drop(['status'],axis=1)
y=ds['status']

x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.2,random_state=0, shuffle=True)

model=LogisticRegression()
model.fit(x_train, y_train)

model.score(x_train,y_train)

model.score(x_test,y_test)

res=model.predict([[59,50,57.85,1,46,50]])
res[0]

res=model.predict([[59,60,54,0,60,50]])
res[0]

